import {describe, it, expect, beforeAll} from "vitest";
import {createBoard, getSquareFromAlgebraic, Piece, setPiece} from "@/board";
import {checkFort} from "./checkFort";
import {printBoard} from "@/board/print";
import {initEngine} from "@/engine";

describe('Fort Check Tests', () => {
  beforeAll(() => {
    initEngine()
  })

  it('empty king -> not fort', () => {
    const board = createBoard()
    setPiece(board, getSquareFromAlgebraic('f6'), Piece.KING)
    expect(checkFort(board)).toBeFalsy()
  })

  it('king is surrounded by defenders in center -> not fort', () => {
    const board = createBoard()
    setPiece(board, getSquareFromAlgebraic('f6'), Piece.KING)
    setPiece(board, getSquareFromAlgebraic('e6'), Piece.DEFENDER)
    setPiece(board, getSquareFromAlgebraic('e7'), Piece.DEFENDER)
    setPiece(board, getSquareFromAlgebraic('g6'), Piece.DEFENDER)
    setPiece(board, getSquareFromAlgebraic('g7'), Piece.DEFENDER)
    setPiece(board, getSquareFromAlgebraic('f5'), Piece.DEFENDER)
    setPiece(board, getSquareFromAlgebraic('f8'), Piece.DEFENDER)
    setPiece(board, getSquareFromAlgebraic('e5'), Piece.DEFENDER)
    setPiece(board, getSquareFromAlgebraic('g5'), Piece.DEFENDER)
    setPiece(board, getSquareFromAlgebraic('e8'), Piece.DEFENDER)
    setPiece(board, getSquareFromAlgebraic('g8'), Piece.DEFENDER)
    printBoard(board)
    expect(checkFort(board)).toBeFalsy()
  })

  it('king is surrounded, on edge, impossible to break, king has moves -> fort', () => {
    const board = createBoard()
    setPiece(board, getSquareFromAlgebraic('f1'), Piece.KING)
    setPiece(board, getSquareFromAlgebraic('e1'), Piece.DEFENDER)
    setPiece(board, getSquareFromAlgebraic('e2'), Piece.DEFENDER)
    setPiece(board, getSquareFromAlgebraic('g1'), Piece.DEFENDER)
    setPiece(board, getSquareFromAlgebraic('g2'), Piece.DEFENDER)
    setPiece(board, getSquareFromAlgebraic('f3'), Piece.DEFENDER)
    setPiece(board, getSquareFromAlgebraic('e3'), Piece.DEFENDER)
    setPiece(board, getSquareFromAlgebraic('g3'), Piece.DEFENDER)
    printBoard(board)
    expect(checkFort(board)).toBeTruthy()
  })

  it('king is surrounded, have access to edge but not on edge, impossible to break, king has moves -> not fort', () => {
    const board = createBoard()
    setPiece(board, getSquareFromAlgebraic('f2'), Piece.KING)
    setPiece(board, getSquareFromAlgebraic('e1'), Piece.DEFENDER)
    setPiece(board, getSquareFromAlgebraic('e2'), Piece.DEFENDER)
    setPiece(board, getSquareFromAlgebraic('g1'), Piece.DEFENDER)
    setPiece(board, getSquareFromAlgebraic('g2'), Piece.DEFENDER)
    setPiece(board, getSquareFromAlgebraic('f3'), Piece.DEFENDER)
    setPiece(board, getSquareFromAlgebraic('e3'), Piece.DEFENDER)
    setPiece(board, getSquareFromAlgebraic('g3'), Piece.DEFENDER)
    printBoard(board)
    expect(checkFort(board)).toBeFalsy()
  })

  it('king is surrounded, on edge, impossible to break, king has moves, but attacker inside -> not fort', () => {
    const board = createBoard()
    setPiece(board, getSquareFromAlgebraic('f1'), Piece.KING)
    setPiece(board, getSquareFromAlgebraic('e1'), Piece.DEFENDER)
    setPiece(board, getSquareFromAlgebraic('e2'), Piece.DEFENDER)
    setPiece(board, getSquareFromAlgebraic('g1'), Piece.DEFENDER)
    setPiece(board, getSquareFromAlgebraic('g2'), Piece.DEFENDER)
    setPiece(board, getSquareFromAlgebraic('f4'), Piece.DEFENDER)
    setPiece(board, getSquareFromAlgebraic('e3'), Piece.DEFENDER)
    setPiece(board, getSquareFromAlgebraic('g3'), Piece.DEFENDER)
    setPiece(board, getSquareFromAlgebraic('e4'), Piece.DEFENDER)
    setPiece(board, getSquareFromAlgebraic('g4'), Piece.DEFENDER)
    setPiece(board, getSquareFromAlgebraic('f3'), Piece.ATTACKER)
    printBoard(board)
    expect(checkFort(board)).toBeFalsy()
  })

  it('king is surrounded by attackers like fort -> not fort', () => {
    const board = createBoard()
    setPiece(board, getSquareFromAlgebraic('f2'), Piece.KING)
    setPiece(board, getSquareFromAlgebraic('e1'), Piece.ATTACKER)
    setPiece(board, getSquareFromAlgebraic('e2'), Piece.ATTACKER)
    setPiece(board, getSquareFromAlgebraic('g1'), Piece.ATTACKER)
    setPiece(board, getSquareFromAlgebraic('g2'), Piece.ATTACKER)
    setPiece(board, getSquareFromAlgebraic('f3'), Piece.ATTACKER)
    setPiece(board, getSquareFromAlgebraic('e3'), Piece.ATTACKER)
    setPiece(board, getSquareFromAlgebraic('g3'), Piece.ATTACKER)
    printBoard(board)
    expect(checkFort(board)).toBeFalsy()
  })

  it('king is surrounded, on edge, impossible to break, king has no moves -> not fort', () => {
    const board = createBoard()
    setPiece(board, getSquareFromAlgebraic('f1'), Piece.KING)
    setPiece(board, getSquareFromAlgebraic('e1'), Piece.DEFENDER)
    setPiece(board, getSquareFromAlgebraic('g1'), Piece.DEFENDER)
    setPiece(board, getSquareFromAlgebraic('f2'), Piece.DEFENDER)
    setPiece(board, getSquareFromAlgebraic('e2'), Piece.DEFENDER)
    setPiece(board, getSquareFromAlgebraic('g2'), Piece.DEFENDER)
    printBoard(board)
    expect(checkFort(board)).toBeFalsy()
  })

  it('king is surrounded, on edge, possible to break, king has no moves -> not fort', () => {
    const board = createBoard()
    setPiece(board, getSquareFromAlgebraic('f1'), Piece.KING)
    setPiece(board, getSquareFromAlgebraic('e1'), Piece.DEFENDER)
    setPiece(board, getSquareFromAlgebraic('g1'), Piece.DEFENDER)
    setPiece(board, getSquareFromAlgebraic('f3'), Piece.DEFENDER)
    setPiece(board, getSquareFromAlgebraic('e2'), Piece.DEFENDER)
    setPiece(board, getSquareFromAlgebraic('g2'), Piece.DEFENDER)
    printBoard(board)
    expect(checkFort(board)).toBeFalsy()
  })

  it('king is surrounded, on edge, impossible to break (more difficult edge case), king has no moves -> fort', () => {
    const board = createBoard()
    setPiece(board, getSquareFromAlgebraic('f1'), Piece.KING)
    setPiece(board, getSquareFromAlgebraic('e1'), Piece.DEFENDER)
    setPiece(board, getSquareFromAlgebraic('g1'), Piece.DEFENDER)
    setPiece(board, getSquareFromAlgebraic('f3'), Piece.DEFENDER)
    setPiece(board, getSquareFromAlgebraic('e2'), Piece.DEFENDER)
    setPiece(board, getSquareFromAlgebraic('g2'), Piece.DEFENDER)
    setPiece(board, getSquareFromAlgebraic('e3'), Piece.DEFENDER)
    setPiece(board, getSquareFromAlgebraic('f4'), Piece.DEFENDER)
    setPiece(board, getSquareFromAlgebraic('g3'), Piece.DEFENDER)
    setPiece(board, getSquareFromAlgebraic('d2'), Piece.DEFENDER)
    setPiece(board, getSquareFromAlgebraic('h2'), Piece.DEFENDER)
    setPiece(board, getSquareFromAlgebraic('i2'), Piece.DEFENDER)
    setPiece(board, getSquareFromAlgebraic('c2'), Piece.DEFENDER)
    printBoard(board)
    expect(checkFort(board)).toBeTruthy()
  })
})
