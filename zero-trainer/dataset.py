"""
Binary dataset reader for AlphaZero self-play data generated by Rust MCTS.

Sample binary format (variable-length):
    BitPosition     49 bytes   (3×16 bytes planes + 1 byte stm)
    LegalMask       605 bytes  (4840 bits packed)
    policy_len      u16        (2 bytes)
    PolicyTarget×N  N×4 bytes  (move_index u16 + visits u16 each)
    value           i8         (1 byte)

Network input planes (6 × 11 × 11):
    0: attackers   (from BitPosition planes[0..16])
    1: defenders   (from BitPosition planes[16..32])
    2: king        (from BitPosition planes[32..48])
    3: side-to-move (all 1s if stm==1, else all 0s)
    4: throne      (square 60)
    5: corners     (squares 0, 10, 110, 120)
"""

from __future__ import annotations

import struct
from pathlib import Path

import numpy as np
import torch
from torch.utils.data import Dataset

BOARD_SIZE = 11
SQS = BOARD_SIZE * BOARD_SIZE  # 121
NUM_PLANES = 6
POLICY_SIZE = 4840

BIT_POSITION_BYTES = 49  # 48 planes + 1 stm
LEGAL_MASK_BYTES = 605   # ceil(4840 / 8)
FIXED_HEADER = BIT_POSITION_BYTES + LEGAL_MASK_BYTES + 2  # 656 bytes before policy entries

THRONE_SQ = 5 * 11 + 5   # 60
CORNERS_SQ = [0, 10, 110, 120]

# Precompute static planes (throne + corners) once
_THRONE_PLANE = np.zeros(SQS, dtype=np.float32)
_THRONE_PLANE[THRONE_SQ] = 1.0

_CORNERS_PLANE = np.zeros(SQS, dtype=np.float32)
for _sq in CORNERS_SQ:
    _CORNERS_PLANE[_sq] = 1.0

# ============================================================
# Board symmetry augmentation (D4: 4 rotations + 4 reflections)
# Directions: Up=0, Down=1, Left=2, Right=3
# ============================================================
_N = BOARD_SIZE - 1  # 10

_SYM_POS = [
    lambda r, c: (r, c),
    lambda r, c: (c, _N - r),
    lambda r, c: (_N - r, _N - c),
    lambda r, c: (_N - c, r),
    lambda r, c: (r, _N - c),
    lambda r, c: (_N - r, c),
    lambda r, c: (c, r),
    lambda r, c: (_N - c, _N - r),
]

_DIR_MAPS = [
    [0, 1, 2, 3],  # identity
    [3, 2, 0, 1],  # rot90CW:  Up->Right, Down->Left, Left->Up,    Right->Down
    [1, 0, 3, 2],  # rot180:   Up->Down,  Down->Up,   Left->Right, Right->Left
    [2, 3, 1, 0],  # rot270CW: Up->Left,  Down->Right, Left->Down, Right->Up
    [0, 1, 3, 2],  # flipH:    Up->Up,    Down->Down,  Left->Right, Right->Left
    [1, 0, 2, 3],  # flipV:    Up->Down,  Down->Up,    Left->Left,  Right->Right
    [2, 3, 0, 1],  # flipD:    Up->Left,  Down->Right, Left->Up,   Right->Down
    [3, 2, 1, 0],  # flipA:    Up->Right, Down->Left,  Left->Down, Right->Up
]


def _build_policy_perm(sym_idx: int) -> np.ndarray:
    """Precompute gather permutation: new_policy = old_policy[perm]."""
    pos_fn = _SYM_POS[sym_idx]
    dir_map = _DIR_MAPS[sym_idx]
    DIRS, MAX_DIST = 4, 10
    perm = np.empty(POLICY_SIZE, dtype=np.int32)
    for sq in range(SQS):
        r, c = sq // BOARD_SIZE, sq % BOARD_SIZE
        new_r, new_c = pos_fn(r, c)
        new_sq = new_r * BOARD_SIZE + new_c
        for d in range(DIRS):
            new_d = dir_map[d]
            for dist in range(MAX_DIST):
                old_idx = sq * (DIRS * MAX_DIST) + d * MAX_DIST + dist
                new_idx = new_sq * (DIRS * MAX_DIST) + new_d * MAX_DIST + dist
                perm[new_idx] = old_idx
    return perm


# Precomputed permutations as torch tensors (computed once at import)
_POLICY_PERMS = [torch.from_numpy(_build_policy_perm(i)).long() for i in range(8)]


def _apply_board_sym(planes: np.ndarray, sym_idx: int) -> np.ndarray:
    """Apply spatial symmetry to board planes (NUM_PLANES, 11, 11)."""
    if sym_idx == 1:
        return np.ascontiguousarray(np.rot90(planes, k=3, axes=(1, 2)))
    elif sym_idx == 2:
        return np.ascontiguousarray(np.rot90(planes, k=2, axes=(1, 2)))
    elif sym_idx == 3:
        return np.ascontiguousarray(np.rot90(planes, k=1, axes=(1, 2)))
    elif sym_idx == 4:
        return np.ascontiguousarray(np.flip(planes, axis=2))
    elif sym_idx == 5:
        return np.ascontiguousarray(np.flip(planes, axis=1))
    elif sym_idx == 6:
        return np.ascontiguousarray(np.swapaxes(planes, 1, 2))
    else:  # sym_idx == 7
        return np.ascontiguousarray(np.swapaxes(np.rot90(planes, k=2, axes=(1, 2)), 1, 2))


def _unpack_bits(data: bytes, num_bits: int) -> np.ndarray:
    """Unpack packed bytes into a float32 array of 0s and 1s."""
    raw = np.frombuffer(data, dtype=np.uint8)
    bits = np.unpackbits(raw, bitorder="little")[: num_bits]
    return bits.astype(np.float32)


def _build_offset_index(path: Path) -> np.ndarray:
    """Scan the binary file and return an array of byte offsets for each sample.

    Only reads the minimum needed (policy_len field) to compute sample sizes.
    """
    file_size = path.stat().st_size
    offsets: list[int] = []
    pos = 0
    with open(path, "rb") as f:
        while pos < file_size:
            offsets.append(pos)
            # Skip fixed header to reach policy_len
            f.seek(pos + BIT_POSITION_BYTES + LEGAL_MASK_BYTES)
            raw = f.read(2)
            if len(raw) < 2:
                break
            policy_len = struct.unpack("<H", raw)[0]
            # Advance past policy entries + value byte
            pos += FIXED_HEADER + policy_len * 4 + 1
    return np.array(offsets, dtype=np.int64)


def _read_sample_at(f, offset: int) -> tuple[torch.Tensor, torch.Tensor, torch.Tensor, torch.Tensor]:
    """Read and decode a single sample from an open file handle at the given offset."""
    f.seek(offset)

    # BitPosition: 49 bytes
    planes_bytes = f.read(48)
    stm = f.read(1)[0]

    # LegalMask: 605 bytes
    legal_mask_bytes = f.read(LEGAL_MASK_BYTES)

    # policy_len: u16
    policy_len = struct.unpack("<H", f.read(2))[0]

    # PolicyTarget × N: each 4 bytes (move_index u16, visits u16)
    policy_raw = f.read(policy_len * 4)

    # value: i8
    value = struct.unpack("<b", f.read(1))[0]

    # -- Decode planes --
    attackers = _unpack_bits(planes_bytes[0:16], SQS)
    defenders = _unpack_bits(planes_bytes[16:32], SQS)
    king = _unpack_bits(planes_bytes[32:48], SQS)
    stm_plane = np.full(SQS, float(stm), dtype=np.float32)

    board_planes = np.stack([
        attackers, defenders, king, stm_plane, _THRONE_PLANE, _CORNERS_PLANE,
    ]).reshape(NUM_PLANES, BOARD_SIZE, BOARD_SIZE)

    # -- Legal mask --
    legal_mask = _unpack_bits(legal_mask_bytes, POLICY_SIZE)

    # -- Policy target: sparse → dense, then normalize --
    policy_visits = np.zeros(POLICY_SIZE, dtype=np.float32)
    for i in range(policy_len):
        off = i * 4
        move_index = struct.unpack_from("<H", policy_raw, off)[0]
        visits = struct.unpack_from("<H", policy_raw, off + 2)[0]
        policy_visits[move_index] = float(visits)

    # Sharpen visit distribution with temperature (lower = sharper)
    POLICY_TARGET_TEMP = 0.5
    if POLICY_TARGET_TEMP != 1.0:
        policy_visits = np.power(policy_visits, 1.0 / POLICY_TARGET_TEMP)

    visit_sum = policy_visits.sum()
    if visit_sum > 0:
        policy_visits /= visit_sum

    return (
        torch.from_numpy(board_planes),
        torch.from_numpy(legal_mask).bool(),
        torch.from_numpy(policy_visits),
        torch.tensor(np.float32(value)),
    )


class SelfPlayDataset(Dataset):
    """
    PyTorch dataset that reads self-play samples lazily from disk.

    Only an index of byte offsets is kept in memory.  Each __getitem__
    call reads and decodes a single sample from the file, so RAM usage
    stays constant regardless of dataset size.

    Compatible with DataLoader(num_workers>0): each worker opens its
    own file handle on first access (stored as an instance attribute
    that lives in the worker process).

    Args:
        path: path to binary data file
        window_size: if > 0, only use the last `window_size` samples
    """

    def __init__(self, path: Path | str, window_size: int = 0, augment: bool = True) -> None:
        self._path = Path(path)
        offsets = _build_offset_index(self._path)

        if window_size > 0 and len(offsets) > window_size:
            offsets = offsets[-window_size:]

        self._offsets = offsets
        self._augment = augment
        # File handle opened lazily per worker process
        self._fh = None

    def __len__(self) -> int:
        return len(self._offsets)

    def _get_fh(self):
        """Return an open file handle, creating one if needed (per-worker)."""
        if self._fh is None:
            self._fh = open(self._path, "rb")
        return self._fh

    def __getitem__(self, idx: int) -> tuple[torch.Tensor, torch.Tensor, torch.Tensor, torch.Tensor]:
        fh = self._get_fh()
        planes, legal_mask, policy, value = _read_sample_at(fh, int(self._offsets[idx]))

        if self._augment:
            sym = int(np.random.randint(8))
            if sym > 0:
                planes = torch.from_numpy(_apply_board_sym(planes.numpy(), sym))
                perm = _POLICY_PERMS[sym]
                policy = policy[perm]
                legal_mask = legal_mask[perm]

        return planes, legal_mask, policy, value

    def __del__(self) -> None:
        if self._fh is not None:
            self._fh.close()
